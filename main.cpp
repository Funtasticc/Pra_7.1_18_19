#include <iostream>
#include <string>
using namespace std;

int main()
{
	int sudoku[9][9] = { };
	string eingabe[11] = { };
	int m = 0, z = 0;
	char c = 0;

	for (int i = 0; i < 11; i++)
	{

		if (i == 0)
			cout << "Bitte geben Sie das Sudoku ein:" << endl;

		getline(cin, eingabe[i]);

		m = 0;

		for (int k = 0; k < eingabe[i].size(); k++)
		{
			c = eingabe[i].at(k);

			if ((c >= '1') && (c <= '9'))
			{

				sudoku[i][m] = (eingabe[i].at(k) - '0');
				m++;
			}

		}

	}

	for (int y = 0; y <= 8; y++)
	{
	
		for (int x = 0; x <= 8; x++)
		{
			if(x == 0)
			{
				cout << ";";
			}

			cout << sudoku[y][x];
			cout << ";";
			if (x == 2)
				cout << "//";
			if (x == 5)
				cout << "//";
			if (x == 8)
				cout << endl;
		}


	}



	/*cout << sudoku[2][2] << endl;*/
	
	system("PAUSE");
	return 0;
}


